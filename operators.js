// operators 

// Arithmetic operators
let a = 10;
let b = 5;

console.log(a + b); // Addition: 15
console.log(a - b); // Subtraction: 5
console.log(a * b); // Multiplication: 50
console.log(a / b); // Division: 2
console.log(a % b); // Modulus (Remainder): 0
console.log(a ** b); // Exponentiation: 100000

// relational operators

console.log("-------------------------------------------------");
let x=10;
let y=5;
console.log(x > y); // Greater than: true
console.log(x < y); // Less than: false
console.log(x >= y); // Greater than or equal to: true
console.log(x <= y); // Less than or equal to: false
console.log(x === y); // comapare both datatypes and values 
console.log(x == y); // compare only values

console.log("---------------------------------------");

// ternary operators
let age = 20;
let allowed = (age > 18) ? 'Yes' : 'No';
console.log(allowed); // Output: 'Yes'

console.log("------------------------------------------");

// logical operators
let p = true;
let q = false;

console.log(p && q); // Logical AND: false
console.log(p || q); // Logical OR: true
console.log(!p); // Logical NOT: false

console.log("------------------------------------");

